package com.dana.hos.map.module;

public class PhDTO {
	private String ph_id;
	private String ph_name;
	private String ph_address;
	private String ph_lat;
	private String ph_lon;
	private String ph_phone;
	private String ph_url;
	private String ph_category_name;

	public PhDTO() {
	}

	public String getPh_id() {
		return ph_id;
	}

	public void setPh_id(String ph_id) {
		this.ph_id = ph_id;
	}

	public String getPh_name() {
		return ph_name;
	}

	public void setPh_name(String ph_name) {
		this.ph_name = ph_name;
	}

	public String getPh_address() {
		return ph_address;
	}

	public void setPh_address(String ph_address) {
		this.ph_address = ph_address;
	}

	public String getPh_lat() {
		return ph_lat;
	}

	public void setPh_lat(String ph_lat) {
		this.ph_lat = ph_lat;
	}

	public String getPh_lon() {
		return ph_lon;
	}

	public void setPh_lon(String ph_lon) {
		this.ph_lon = ph_lon;
	}

	public String getPh_phone() {
		return ph_phone;
	}

	public void setPh_phone(String ph_phone) {
		this.ph_phone = ph_phone;
	}

	public String getPh_url() {
		return ph_url;
	}

	public void setPh_url(String ph_url) {
		this.ph_url = ph_url;
	}

	public String getPh_category_name() {
		return ph_category_name;
	}

	public void setPh_category_name(String ph_category_name) {
		this.ph_category_name = ph_category_name;
	}

}
