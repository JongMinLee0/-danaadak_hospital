package com.dana.hos.map.module;

public class HosDTO {
	private String hos_id;
	private String hos_name;
	private String hos_address;
	private String hos_lat;
	private String hos_lon;
	private String hos_phone;
	private String hos_url;
	
	public HosDTO() {
	}
	
	public String getHos_id() {
		return hos_id;
	}

	public void setHos_id(String hos_id) {
		this.hos_id = hos_id;
	}

	public String getHos_name() {
		return hos_name;
	}

	public void setHos_name(String hos_name) {
		this.hos_name = hos_name;
	}

	public String getHos_address() {
		return hos_address;
	}

	public void setHos_address(String hos_address) {
		this.hos_address = hos_address;
	}

	public String getHos_lat() {
		return hos_lat;
	}

	public void setHos_lat(String hos_lat) {
		this.hos_lat = hos_lat;
	}

	public String getHos_lon() {
		return hos_lon;
	}

	public void setHos_lon(String hos_lon) {
		this.hos_lon = hos_lon;
	}

	public String getHos_phone() {
		return hos_phone;
	}

	public void setHos_phone(String hos_phone) {
		this.hos_phone = hos_phone;
	}

	public String getHos_url() {
		return hos_url;
	}

	public void setHos_url(String hos_url) {
		this.hos_url = hos_url;
	}
	
}
